const provider = new ethers.providers.Web3Provider(web3.currentProvider);
provider.send("eth_requestAccounts", []).then(() => {
  provider.listAccounts().then((accounts) => {
    const signer = provider.getSigner(accounts[0]);
Tazamat = new ethers.Contract(
      Tazamat_Contract_Address,
      Tazamat_Contract_ABI,
      signer
    );
    Cringe = new ethers.Contract(
      Cringe_Contract_Address,
      Cringe_Contract_ABI,
      signer
    );
  });
});

const storeAddress = '0x961E35CA2B6Bd0766d2B1caCEebEC123d96b80f8';

setInterval(
  function() 
  {
    provider.listAccounts().then(function(result){
Tazamat.balanceOfCringe(result[0]).then(function(balance){
$('#nft_balance').html(parseInt(balance._hex));
});
})
Tazamat.totalSupply().then(function(res){
    $('#total_supply').html(parseInt(res._hex))
});

  }, 3000);
  
  setInterval(
  function() 
  {
    provider.listAccounts().then(function(result){
    for (let i = 0; i < 4; i++) {
        Tazamat.ownerOf(i).then(function(data){
            if (data == storeAddress) {
			    $('#nft_' + i + ' button').html('Buy');	
			    $('#nft_' + i + ' button').attr('onclick', 'buyNFT(' + i + ')');
			    $('#nft_' + i + ' .owner').html('This is store\'s NFT!');
		    }
		    else {
		        $('#nft_' + i + ' button').html('Sell');
		        $('#nft_' + i + ' button').attr('onclick', 'sellNFT(' + i + ')');
		        $('#nft_' + i + ' .owner').html('This is your\'s NFT!');
		    }
        });
        Tazamat.tokenURI(i).then(function(uri){
            uri = 'https://ipfs.filebase.io/ipfs/' + uri.substring(7);
            $('#nft_' + i + ' a').attr("href", uri);
            $.getJSON(uri, function(data) {
                $('#nft_' + i + ' .description').html(data.description);
                $('#nft_' + i + ' .name').html(data.name);
                $('#nft_' + i + ' img').attr('src', data.image);
            });
        });
    }
})

  }, 4000);

